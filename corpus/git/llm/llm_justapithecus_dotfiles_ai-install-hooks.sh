#!/usr/bin/env bash
set -euo pipefail

# Portable script directory resolution (no readlink -f dependency)
SCRIPT_DIR="$(
  src="${BASH_SOURCE[0]}"
  while [[ -L "$src" ]]; do
    dir="$(cd "$(dirname "$src")" && pwd -P)"
    src="$(readlink "$src")"
    [[ "$src" != /* ]] && src="$dir/$src"
  done
  cd "$(dirname "$src")" && pwd -P
)"

# --- Detect git repo ------------------------------------------------------

REPO_ROOT="$PWD"
if git rev-parse --show-toplevel >/dev/null 2>&1; then
  REPO_ROOT="$(git rev-parse --show-toplevel)"
fi

HOOKS_DIR="$REPO_ROOT/.git/hooks"
if [[ ! -d "$HOOKS_DIR" ]]; then
  echo "error: not inside a git repository" >&2
  exit 1
fi

# --- Install pre-push hook ------------------------------------------------

HOOK_FILE="$HOOKS_DIR/pre-push"

if [[ -f "$HOOK_FILE" ]]; then
  echo "warning: $HOOK_FILE already exists" >&2
  echo "Existing content:" >&2
  head -5 "$HOOK_FILE" >&2
  echo "Overwrite? [y/N]" >&2
  read -r REPLY
  if [[ "$REPLY" != "y" && "$REPLY" != "Y" ]]; then
    echo "Aborted."
    exit 0
  fi
fi

cat > "$HOOK_FILE" << 'HOOK'
#!/usr/bin/env bash
# Auto-generated by ai-install-hooks.sh
# Runs ai-check with default bundle before push

if command -v ai-check >/dev/null 2>&1; then
  echo "▶ Running ai-check --bundle default"
  ai-check --bundle default || {
    echo "✖ ai-check failed. Push blocked." >&2
    exit 1
  }
else
  echo "⚠ ai-check not found in PATH; skipping pre-push check" >&2
fi
HOOK

chmod +x "$HOOK_FILE"
echo "✔ Installed pre-push hook: $HOOK_FILE"
